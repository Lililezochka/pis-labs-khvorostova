def print_students(students):

    # Печатаем шапку таблицы (заголовки колонок)
    print(
        "Имя".ljust(12),
        "Фамилия".ljust(12),
        "Экзамены".ljust(35),
        "Оценки".ljust(15),
        "Средний"
    )
    print("-" * 90)  # просто разделительная линия

    # Проходимся по каждому студенту из списка
    for st in students:
        # exams — это список, превращаем его в строку через запятую
        exams_str = ", ".join(st["exams"])
        # marks — тоже список, но из чисел, поэтому сначала переводим в str
        marks_str = ", ".join(str(m) for m in st["marks"])
        # считаем средний балл: сумма оценок / количество
        avg = sum(st["marks"]) / len(st["marks"])

        # Выводим одну строку с данными по студенту
        print(
            st["name"].ljust(12),
            st["surname"].ljust(12),
            exams_str.ljust(35),
            marks_str.ljust(15),
            f"{avg:.2f}"   # форматируем средний балл до двух знаков после запятой
        )


def filter_by_average(students, threshold):
    """
    Функция фильтрует студентов по среднему баллу.
    Оставляет только тех, у кого средний балл > threshold (порог).
    """
    result = []  # сюда будем складывать подходящих студентов

    # пробегаемся по всем студентам
    for st in students:
        avg = sum(st["marks"]) / len(st["marks"])  # считаем средний балл
        if avg > threshold:  # если средний балл больше порога
            result.append(st)  # добавляем студента в результат

    return result  # возвращаем новый список

